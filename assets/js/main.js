document.addEventListener('DOMContentLoaded', function() {
    // Smooth scrolling para los enlaces de navegaci贸n
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                window.scrollTo({
                    top: targetElement.offsetTop - 80,
                    behavior: 'smooth'
                });
            }
        });
    });
    
    // Duplicar contenido del slider para efecto infinito
    const skillsTrack = document.querySelector('.skills-track');
    if (skillsTrack) {
        const skillsItems = skillsTrack.innerHTML;
        skillsTrack.innerHTML += skillsItems;
    }
    
    // Animaci贸n de aparici贸n al hacer scroll
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.1
    };
    
    const observer = new IntersectionObserver(function(entries, observer) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);
    
    // Observar elementos para animaci贸n
    const animatedElements = document.querySelectorAll('.skill-item, .proyecto-card, .copy-example');
    animatedElements.forEach(el => {
        el.style.opacity = '0';
        el.style.transform = 'translateY(20px)';
        el.style.transition = 'opacity 0.6s ease, transform 0.6s ease';
        observer.observe(el);
    });
});